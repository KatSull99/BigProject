public class Reservations
	{
			private String sailTo;
			private String sailFrom;
			private String startingDate;
			private int duration;
			private String roomTypes;
			public String getStartingDate()
				{
					return startingDate;
				}
			public void setStartingDate(String startingDate)
				{
					this.startingDate = startingDate;
				}
			public String getRoomTypes()
				{
					return roomTypes;
				}
			public void setRoomTypes(String roomTypes)
				{
					this.roomTypes = roomTypes;
				}
			public String getSailTo()
				{
					return sailTo;
				}
			public void setSailTo(String sailTo)
				{
					this.sailTo = sailTo;
				}
			public String getSailFrom()
				{
					return sailFrom;
				}
			public void setSailFrom(String sailFrom)
				{
					this.sailFrom = sailFrom;
				}
			public String getDates()
				{
					return startingDate;
				}
			public void setDates(String dates)
				{
					this.startingDate = dates;
				}
			public int getDuration()
				{
					return duration;
				}
			public void setDuration(int duration)
				{
					this.duration = duration;
				}
		
		
	}
